//Exercicio 2

function smallerName(books) {
  let nameBook;
  books.forEach(e => {
    if (!nameBook || e.name.length < nameBook.length) nameBook = e.name;
  });
  return nameBook;
}

assert.strictEqual(smallerName(books), 'Duna');

